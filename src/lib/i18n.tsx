function getStoredLang(): Lang {
  try {
    const v = localStorage.getItem(LANG_KEY)
    if (v === 'zh' || v === 'en') return v
  } catch {}
  return 'zh'
}

/** 支持简单占位符，如 t('key', { tag: '工作' }) => 将 key 中的 {tag} 替换 */
export function t(lang: Lang, key: string, params?: Record<string, string>): string {
  let s = messages[lang]?.[key] ?? messages.zh[key] ?? key
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), v)
    })
  }
  return s
}

type I18nContextValue = {
  lang: Lang
  setLang: (l: Lang) => void
  t: (key: string, params?: Record<string, string>) => string
}

const I18nContext = createContext<I18nContextValue | null>(null)

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>(getStoredLang)

  useEffect(() => {
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en'
    try {
      localStorage.setItem(LANG_KEY, lang)
    } catch {}
  }, [lang])

  const value: I18nContextValue = {
    lang,
    setLang: setLangState,
    t: (key: string, params?: Record<string, string>) => t(lang, key, params),
  }

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n(): I18nContextValue {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be used within I18nProvider')
  return ctx
}
